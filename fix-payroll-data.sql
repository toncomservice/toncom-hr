-- 1) เพิ่มคอลัมน์ resign_date ในตาราง profiles
--    (โค้ดตอนกดลาออกเขียนค่านี้อยู่แล้ว แต่คอลัมน์ยังไม่มี ทำให้ UPDATE ล้มเหลว
--     คนที่กดลาออกจึงไม่ถูกบันทึกจริง พอรีเฟรชกลับมา active เหมือนเดิม)
ALTER TABLE profiles
  ADD COLUMN IF NOT EXISTS resign_date DATE;

-- 2) รวมข้อมูลของ "ตอง" ให้เป็นชื่อเดียว
--    โปรไฟล์เดิม username = 'tongg' แต่ประวัติเงินเบิก/เงินพิเศษ/ค่าแรง/ลงเวลา
--    ทั้งหมดอยู่ใต้ 'tong' -> เปลี่ยน username โปรไฟล์เป็น 'tong' ให้ตรงกัน
UPDATE profiles SET username = 'tong' WHERE username = 'tongg';

-- 3) ใส่วันที่ลาออกให้ตอง (ปัจจุบัน active=false แต่ไม่มีวันลาออก
--    ทำให้ระบบคิดค่าแรงถึง "วันนี้" ซึ่งเกินจริง)
--    >>> แก้วันที่ '2026-04-30' ให้ตรงกับวันลาออกจริงก่อน Run <<<
UPDATE profiles
  SET resign_date = '2026-04-30'
  WHERE username = 'tong';

